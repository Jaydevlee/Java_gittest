package chap06.chap6_4.연습문제;

import java.util.Scanner;

public class Calculator {
	double getAdd(double num1, double num2) {
		return num1 + num2;
	}
	double getSub(double num1, double num2) {
		return num1 - num2;
	}
	double getMul(double num1, double num2) {
		return num1 * num2;
	}
	double getADiv(double num1, double num2) {
		return num1 / num2;
	}
}
